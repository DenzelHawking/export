  <div class="news-title">Положение об Агентстве по экспорту при Правительстве Республики Таджикистан</div>
  <div class="news-description">
    <ol class="ministry-list">
      <h3>Общие положения</h3>
      <li>Настоящее Положение определяет цели, полномочия и организационно - правовые основы деятельности
        Агентства по экспорту при Правительстве Республики Таджикистан (далее - Агентство).</li>
      <li>Агентство является государственным центральным органом исполнительной власти, осуществляющим
        специальные исполнительные функции и другие функции, определенные в сфере деятельности по развитию
        содействия экспорта.</li>
      <li>Агентство в своей деятельности руководствуется Конституции Республики Таджикистан, конституционными
        законами, кодексами,- законами, международными правовыми актами, признанными Таджикистаном, совместными
        постановлениями Маджлиси милли и Маджлиси намояндагон Маджлиси Оли Республики Таджикистан,
        постановлениями Маджлиси милли и Маджлиси намояндагон Маджлиси Оли Республики Таджикистан, указами
        Президента Республики Таджикистан, постановлениями Правительства Республики Таджикистан, иными
        нормативными правовыми актами и настоящим Положением.</li>
      <li>Агентство является юридическим лицом, имеет печать с изображением Государственного герба Республики
        Таджикистан и своим наименованием на государственном, русском и английском языках.</li>
      <li>Полное наименование Агентства:
        <div class="ministry-item">на государственном языке «Агентии содирот назди Х,УкУм£1ти Чумхурии
          Тоцикистон»;</div>
        <div class="ministry-item">на русском языке «Агентство по экспорту при Правительстве Республики
          Таджикистан»;</div>
        <div class="ministry-item">на английском языке «Agency for export under the Government of the Republic
          of Tajikistan».</div>
      </li>
      <h3>Основные цели Агентства</h3>
      <li>Основными целями Агентства являются следующие:
        <div class="ministry-item">содействие устойчивому развитию количества и качества экспорта товаров,
          продукции и услуг отечественного производства за рубеж;</div>
        <div class="ministry-item">содействие в повышении экспортного потенциала страны, обеспечивающего
          конкурентоспособные условия для отечественных товаров и услуг на зарубежных рынках;</div>
        <div class="ministry-item">содействие в привлечении прямых покупателей, надежных посредников, сетей
          продаж оптовых товаров и других торговых партнеров в зарубежных странах для продажи и экспорта товаров
          и услуг предприятий и компаний отечественного производства.</div>
      </li>
      <h3>Полномочия Агентства</h3>
      <li>Агентство выполняет следующие задачи:
        <div class="ministry-item">содействие в увеличении количества и номенклатуры производимых
          конкурентоспособных и ориентированных на экспорт товаров, и услуг;</div>
        <div class="ministry-item">сотрудничество и содействие в разработке и реализации экспортной стратегии,
          маркетинга, занимание положения на рынке и менеджмента поддержки внешнеэкономической деятельности
          хозяйствующих субъектов;</div>
        <div class="ministry-item">информационная и консультативная поддержка и маркетинговое - аналитическое
          содействие для развития экспорта товаров и продукции производимых хозяйствующими субъектами;</div>
        <div class="ministry-item">привлечение хозяйствующих субъектов к внешнеэкономической деятельности с
          использованием международных сетей поддержки предпринимательства;</div>
        <div class="ministry-item">организация хозяйствующих субъектов за счет бюджетных и внебюджетных средств
          для закупки, запасов, хранение, упаковки и экспорта конкурентоспособных товаров за рубежом;</div>
        <div class="ministry-item">содействие в приведении отечественных товаров и услуг в соответствие с
          требованиями и нормами зарубежных стран;</div>
        <div class="ministry-item">содействие и участие в проведении рыночных исследований для вывоза товаров и
          услуг отечественных хозяйствующих субъектов на рынки зарубежных стран;</div>
        <div class="ministry-item">участие и содействие в разработке программы образования основных групп
          (кластеров) экспортных товаров, в том числе центры кластерного развития и механизмы их реализации;
        </div>
        <div class="ministry-item">составление реестра хозяйствующих субъектов, заняты экспортом отечественных
          товаров и услуг, обеспечение указанных предприятий нормативно - технической документацией и принятие
          практических мер по внедрению международных стандартов в этих предприятиях;</div>
        <div class="ministry-item">содействие доступу хозяйствующих субъектов к стандартам национальным,
          региональным и международным техническим регламентам посредством получения стандартов, перевода,
          электронной базы данных о стандартах;</div>
        <div class="ministry-item">совместно с уполномоченными органами и экспертами содействовать хозяйствующим
          субъектам для предоставления отечественных товаров и продукции в зарубежных рынках, связанные с
          национальным брендом, упаковкой, расфасовкой, маркированием, сертификацией, заключением
          аккредитованной лаборатории и содействием в его перевозке и продажи за рубежом;</div>
        <div class="ministry-item">представление отечественных товаров на внешнем рынке, импорт и его продажа за
          границу в установленном порядке;</div>
        <div class="ministry-item">участие в международных, региональных и национальных выставках с целью
          продвижения бренда отечественной продукции;</div>
        <div class="ministry-item">совместно с уполномоченными органами и хозяйствующими субъектами разработать
          информацию о товарной знаке, указательных знаков, товарных сведениях и коммерческом названии товаров,
          ориентированных на экспорт на языке потребителя;</div>
        <div class="ministry-item">представление предложений уполномоченным органам по нормам тарифных перевозок
          для отечественных экспортеров товаров и продукции, меры по сокращению объема и перечня экспортных
          товаров и продукции в зависимости от региона республики и производственного потенциала;</div>
        <div class="ministry-item">представление предложений уполномоченным органам и участие в создании
          логистических центров, пунктов погрузки и перевозки экспортных товаров в места благоприятные для
          хозяйствующих субъектов;</div>
        <div class="ministry-item">участие в разработке нормативных правовых актов об уменьшении
          административных и технических барьеров при экспорте и транзите товаров и продукции, правильное
          определение таможенной стоимости товаров, контроль качества и безопасности, а также устранение других
          проблем в торговой деятельности и услугах;</div>
        <div class="ministry-item">установление сотрудничества с уполномоченными органами по ускорению процесса
          разработки стандартов и технических регламентов, международной аккредитации лабораторий, реализации
          единого окна по оформлению экспорта и импорта, и упрощения торговых процедур;</div>
        <div class="ministry-item">проведение мониторинга исполнения нормативных правовых актов об устранении
          административных и коммерческих барьеров при экспорте;</div>
        <div class="ministry-item">содействие по внедрению в практику предприятий ориентированных на экспорт
          международных стандартов качества ИСО 9000, ИСО 14000, ИСО 22000 и НАССР;</div>
        <div class="ministry-item">содействие в создании форм и методов приграничной и транзитной торговли;
        </div>
        <div class="ministry-item">принятие необходимых мер в вопросе представления товаров отечественного
          производства, содействие в проведении маркетингового исследования нового товара и поиск новых рынков
          для экспорта товаров;</div>
        <div class="ministry-item">представление предложений по организации и обновлению сайтов хозяйствующих
          субъектов, ориентированных на экспорт, в информационно-коммуникационных сетях интернет, базы данных
          государственного унитарного предприятия «Единое окно» при Таможенной службе и сайта Министерства
          экономического развития и торговли (http://medt.tj) с учетом содержания информации о субъекте, а также
          производства товара (исполнение работ, предоставление услуг) на государственном и иностранном языках;
        </div>
        <div class="ministry-item">ежегодная организация и проведение конкурса «Лучший экспортер Таджикистана»
          среди хозяйствующих субъектов в сотрудничестве с соответствующими министерствами и ведомствами;</div>
        <div class="ministry-item">планирование визита представителей компаний и предприятий и деловых кругов
          иностранных предпринимателей в Республику Таджикистан с целью содействия в развитии сотрудничества с
          хозяйствующими субъектами страны и заключения договоров и соглашений, экспортных контрактов и
          меморандумов;</div>
        <div class="ministry-item">с целью повышения уровня знаний хозяйствующих субъектов и государственных
          служащих по теме экспортная деятельность в рамках международного сотрудничества, позиционирование и
          поиск новых мест на рынке, а также современные методы проведения переговоров организация обучающих
          семинаров, тренингов, круглых столов, конференций, форумов, мастер-классов и других публичных
          мероприятий;</div>
        <div class="ministry-item">содействие в повышении международного статуса и рейтинга торговли Республики
          Таджикистан;</div>
        <div class="ministry-item">другие задачи, предусмотренные действующим законодательством в сфере развития
          экспорта.</div>
      </li>
      <li>Агентство в целях реализации полномочий в установленной сфере деятельности имеет право:
        <div class="ministry-item">представлять Правительству Республики Таджикистан предложения по
          совершенствованию нормативных правовых актов и другие важные вопросы в сфере экспортной деятельности;
        </div>
        <div class="ministry-item">в рамках своих полномочий привлекать в установленном порядке специалистов
          государственных органов, местных органов государственной власти, научно - исследовательских учреждений
          и иных организаций для решения существующих вопросов;</div>
        <div class="ministry-item">в рамках своих полномочий запрашивать и представлять сведения по решению
          существующих вопросов;</div>
        <div class="ministry-item">командировать работников для участия в конференциях, обучающих семинарах и
          других мероприятиях, в том числе на территории республики;</div>
        <div class="ministry-item">осуществлять обучение работников с целью повышения их квалификации, в том
          числе за счет средств Агентства;</div>
        <div class="ministry-item">вносить предложения по предоставлению экспортных стимулов, льгот и гарантий;
        </div>
        <div class="ministry-item">осуществлять иные полномочия и права в соответствии с законодательством
          Республики Таджикистан.</div>
      </li>
      <h3>Организация деятельности Агентства</h3>
      <li>Руководство деятельностью Агентства осуществляет директор, назначаемый на должность и освобождаемый от
        должности Правительством Республики Таджикистан.</li>
      <li>Директор имеет двоих заместителей, в том числе одного первого заместителя, назначаемых на должность и
        освобождаемых от должности Правительством Республики Таджикистан.</li>
      <li>В отсутствие директора Агентства его обязанности исполняет один из заместителей директора.</li>
      <li>Директор Агентства:
        <div class="ministry-item">осуществляет общее руководство деятельностью агентства;</div>
        <div class="ministry-item">распределяет обязанности между своими заместителями;</div>
        <div class="ministry-item">в пределах своих полномочий, представляет Агентство в отношениях с
          государственными органами, местными органами государственной власти, предприятиями и организациями
          Республики Таджикистан, государственными органами иностранных государств и международными
          организациями, проводит переговоры и в установленном порядке подписывает договоры о взаимодействии и
          сотрудничестве по вопросам, отнесенным к компетенции агентства;</div>
        <div class="ministry-item">утверждает структуру и штатное расписание Агентства в соответствии с типовыми
          требованиями в рамках фонда заработной платы и количества работников, установленных Правительством
          Республики Таджикистан, единую смету доходов и расходов агентства в рамках средств, предусмотренных
          для соответствующего периода в республиканском бюджете;</div>
        <div class="ministry-item">представляет для утверждения Правительству Республики Таджикистан штатное
          расписание и. структуру Агентства в рамках установленных штатных единиц;</div>
        <div class="ministry-item">в пределах своих полномочий для исполнения обязательств агентства издает
          распоряжения, дает указания и контролирует ход их исполнения;</div>
        <div class="ministry-item">в соответствии с законодательством назначает на должность и освобождает от
          должности работников агентства;</div>
        <div class="ministry-item">утверждает положения о структурных подразделениях, обязанности и должностные
          инструкции сотрудников Агентства;</div>
        <div class="ministry-item">утверждает ежегодный план работы Агентства;</div>
        <div class="ministry-item">несет ответственность за выполнение задач и функций, возложенных на
          Агентство;</div>
        <div class="ministry-item">осуществляет иные полномочия и функции по руководству деятельностью Агентства
          в соответствии с законодательством Республики Таджикистан.</div>
      </li>
      <h3>Финансовая и хозяйственная деятельность</h3>
      <li>Агентство в целях осуществления своей деятельности использует:
        <div class="ministry-item">имущество, находящееся в государственной собственности, закрепленное за ним
          на праве оперативного управления;</div>
        <div class="ministry-item">финансовые средства, имущество и иные объекты, нематериальные ценности в виде
          продуктов интеллектуального и творческого труда, являющиеся результатом его деятельности.</div>
      </li>
      <li>Финансирование деятельности материальное поощрение и социальные выплаты, содержание и развитие
        материально-технической базы Агентства, осуществляются за счет средств республиканского бюджета
        Республики Таджикистан и иных источников, не запрещенных законодательством Республики Таджикистан.</li>
